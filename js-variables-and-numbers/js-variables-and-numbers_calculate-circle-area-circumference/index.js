let circleRadius = 5;
let circleArea = Math.PI * Math.pow(circleRadius, 2);
// console.log(circleArea);
let circleCircumference = 2 * Math.PI * circleRadius;
// console.log(circleCircumference);
console.log("For a circle with a radius of 5 units:");
console.log(
  "- The area is approximately",
  circleArea.toFixed(2),
  "square units."
);
console.log(
  "- The circumference is approximately",
  circleCircumference.toFixed(2),
  "units."
);
